        private void btnSave_Click(object sender, EventArgs e)
        {
            string strPermission = ReadPermissionItem();
          
            try
            {
                dt = FillData("WHERE RoleName = '" + Txtsearch.Text + " ' ");
                if (commandAction == "Add")
                {
                    if (Txtsearch.Text != "")
                    {  
                        if (DBFun.IsNullOrEmpty(dt))
                        {
                            Provider.InsertPermitions(Txtsearch.Text, strPermission);
                          
                            MessageBox.Show("Permition Added Succesfully", "Message", MessageBoxButtons.OK, MessageBoxIcon.Information);
                            chbSelectAll.Checked = false;
                            FillData();
                        }
                        else
                        {
                            MessageBox.Show("Permitions Names Already Exist Please find another id", "Message", MessageBoxButtons.OK, MessageBoxIcon.Information);
                        }
                    }

                    else
                    {
                        MessageBox.Show("Please Fill All Fields", "Message", MessageBoxButtons.OK, MessageBoxIcon.Information);
                    }
                }
                if (commandAction == "Edit")
                {
                    dt = FillData("WHERE RoleName = '" + CombPermitions.Text + " ' ");
                  //  string combper = Convert.ToString(dt.Rows[0]["RoleName"]);
                    if (CombPermitions.Text != "Please select any value")
                    {
                        Provider.UpdatePermitions(CombPermitions.Text, strPermission);
                        CommandStatus("101011");
                        CombPermitions.Enabled = true;
                        ItemDataStatus('E', false, false,false);
                        commandAction = "";
                        MessageBox.Show("Updated Done Successfuly", "Message", MessageBoxButtons.OK, MessageBoxIcon.Information);
                        chbSelectAll.Checked = false;
                        FillData();
                    }
                    else
                    {
                        MessageBox.Show("Please Fill All Fields", "Message", MessageBoxButtons.OK, MessageBoxIcon.Information);
                    }
                }
            }

            catch (Exception ex)
            {
                MessageBox.Show("Procces Not compleate please contact administrator", "Message", MessageBoxButtons.OK, MessageBoxIcon.Error);
            }
        }

