        #region check box
        private void uncheck()
        {
            if (chbSelectAll.Checked)
            {
                foreach (Control c in AddPermisionsGroup.Controls)
                {
                    if (c.GetType() == typeof(CheckBox))
                    {
                        ((CheckBox)c).Checked = true;
                    }
                }
            }
            else
            {
                foreach (Control c in AddPermisionsGroup.Controls)
                {
                    if (c.GetType() == typeof(CheckBox))
                    {
                        ((CheckBox)c).Checked = false;
                    }
                }
            }
        }
